package dto;

public class TutorDTO {
	
	
	private String tutor_cd;
	private String tutor_name;
	private String tutor_member_cd;
	
	public String getTutor_cd() {
		return tutor_cd;
	}
	public void setTutor_cd(String tutor_cd) {
		this.tutor_cd = tutor_cd;
	}
	public String getTutor_name() {
		return tutor_name;
	}
	public void setTutor_name(String tutor_name) {
		this.tutor_name = tutor_name;
	}
	public String getTutor_member_cd() {
		return tutor_member_cd;
	}
	public void setTutor_member_cd(String tutor_member_cd) {
		this.tutor_member_cd = tutor_member_cd;
	}
	
	
	
	
}
